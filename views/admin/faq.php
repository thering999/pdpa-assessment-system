  <h2>FAQ / คู่มือการใช้งานระบบ</h2>
  <h3>Q: วิธีนำเข้า Excel?</h3>
  <p>ไปที่เมนู Admin &gt; นำเข้า XLSX เลือกไฟล์ Excel ที่เตรียมไว้ตามฟอร์แมต แล้วกด Import</p>
  <h3>Q: วิธี export ข้อมูล?</h3>
  <p>ไปที่เมนู Admin &gt; Export เลือกรูปแบบ (Excel, PDF, CSV) แล้วดาวน์โหลดไฟล์</p>
  <h3>Q: วิธีดูประวัติการประเมินย้อนหลัง?</h3>
  <p>ไปที่เมนู "ประวัติการประเมิน" จะเห็นรายการย้อนหลัง สามารถเปรียบเทียบหรือ export ได้</p>
  <h3>Q: วิธี reset password?</h3>
  <p>กด "ลืมรหัสผ่าน?" ที่หน้า Login กรอกอีเมล ระบบจะส่งลิงก์รีเซ็ตไปให้ (demo: แสดงลิงก์บนหน้าจอ)</p>
  <h3>Q: วิธีดู audit log?</h3>
  <p>เฉพาะ admin: ไปที่เมนู Admin &gt; Audit Log จะเห็นบันทึกการกระทำสำคัญ</p>
  <h3>Q: ติดต่อ support ได้อย่างไร?</h3>
  <p>ดูข้อมูลติดต่อในหน้าแรก หรือสอบถามแอดมินระบบ</p>
